base = float(input("Escribe la longitud de la base del triangulo: "))
altura = float(input("Escribe la longitud de la altura del triangulo: "))
lado = float(input("Escribe la longitud del lado del triangulo: "))
area = (base*altura)/2
per = (lado*3)
print(f'El área del triangulo es: {area}.')
print(f'El perímetro del triangulo es: {per}.')